public class Array {
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 4};
        for (int i : array) { // Aqui eu estou dizendo que para cada elemento contido no array, eu vou executar o código abaixo.
            // : Nesse caso é o operador de iteração.
            System.out.println(i);
        }
        
    }

}
